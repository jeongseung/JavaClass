package basic03.oper;

public class Optest08 {
	public static void main(String[] args) {
		int a = 5;
		
		a += 5; // a = a + 5
		System.out.println(a);
		

		a -= 5; // a = a - 5
		System.out.println(a);

		a *= 5; // a = a * 5
		System.out.println(a);

		a /= 5; // a = a / 5
		System.out.println(a);

		a %= 5; // a = a 5 5
		System.out.println(a);
		
	}

}
